# variables

# best practices
    # snake_case
    # start with lowercase or underscore
        # underscore indicate private variables
    # letters, numbers and underscores
    # case sensative
    # cannot override keywords 
        # include but not limited to print, and, class, global, etc. 

iq = 190 # assigned variable
print(iq) # -> 190

user_iq = iq / 4
print(user_iq) # -> 47.5

a,b,c = 1,2,3
print(a) # -> 1
print(b) # -> 2
print(c) # -> 3

# constants
    # should be in all capitals

PI = 3.141592
print(PI)

